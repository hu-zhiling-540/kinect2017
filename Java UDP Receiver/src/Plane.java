
public class Plane {

	double a;
	double b;
	double c;
	double d;
	
//	n = a normal vector
//	(x0, y0, z0) point on the plane
			
	/**
	 * scalar equation of plane
	 * a normal vector for the plane
	 * @param a
	 * @param b
	 * @param c
	 * @param d
	 */
	public plane(double a, double b, double c, double d)	{
		this.a = a;
		this.b = b;
		this.c = c;
		this.d = d;
	}
}
